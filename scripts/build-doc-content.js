#!/usr/bin/env node
/**
 * Bundle docs/*.md into js/doc-content.js for offline / file:// safe viewing.
 * Run: node scripts/build-doc-content.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const DOCS_DIR = path.join(ROOT, 'docs');

const FILES = {
  problem: 'problem.md',
  decisions: 'decisions.md',
  process: 'process.md',
  'heatmap-rules': 'heatmap-rules.md',
  layout: 'layout.md',
  states: 'states.md',
  submit: 'submit.md',
  structure: 'structure.md',
  answers: path.join('..', '제출문항.md'),
};

const content = {};
for (const [key, file] of Object.entries(FILES)) {
  const fullPath = file.startsWith('..')
    ? path.join(ROOT, path.basename(file))
    : path.join(DOCS_DIR, file);
  content[key] = fs.readFileSync(fullPath, 'utf8');
}

const out = `/* Auto-generated by scripts/build-doc-content.js — do not edit by hand */\nconst DOC_CONTENT = ${JSON.stringify(content)};\n`;
if (process.argv.includes('--stdout')) {
  process.stdout.write(out);
} else {
  fs.writeFileSync(path.join(ROOT, 'docs', 'doc-content.js'), out);
  console.log('Wrote docs/doc-content.js (' + Object.keys(content).length + ' docs)');
}
